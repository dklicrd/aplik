export function getAllPresupuestos() {
  const all = [];
  for (const year of years) {
    const list = rawData[year] || [];
    for (const row of list) {
      all.push({
        year: parseInt(year),
        cliente: row[0],
        descripcion: row[1],
        fecha: row[2],
        presupuesto: row[3],
        orden: row[4],
        estatus: row[5],
        factura: row[6],
        zona: row[7],
      });
    }
  }
  return all;
}

